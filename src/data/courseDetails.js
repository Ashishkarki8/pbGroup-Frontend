// Search courses by title (optimized with includes)
export const searchCoursesByTitle = (searchTerm) => {
  if (!searchTerm || searchTerm.length < 2) return [];
  
  const lowerSearchTerm = searchTerm.toLowerCase();
  return courseDetails.filter(course => 
    course.title.toLowerCase().includes(lowerSearchTerm) ||
    course.subtitle.toLowerCase().includes(lowerSearchTerm)
  );
};

// Get random courses (useful for recommendations)
export const getRandomCourses = (limit = 3, excludeSlug = null) => {
  let availableCourses = courseDetails;
  
  if (excludeSlug) {
    availableCourses = courseDetails.filter(course => course.slug !== excludeSlug);
  }
  
  const shuffled = [...availableCourses].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, limit);
};

// Essential utility functions
export const getRatingStars = (rating) => {
  const fullStars = Math.floor(rating);
  const hasHalfStar = rating % 1 !== 0;
  const stars = new Array(fullStars).fill('full');
  
  if (hasHalfStar) stars.push('half');
  
  while (stars.length < 5) {
    stars.push('empty');
  }
  
  return stars;
};

