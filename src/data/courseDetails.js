// Create Maps for O(1) lookup performance
const courseMapById = new Map();
const courseMapBySlug = new Map();
const slugListCache = courseDetails.map(course => course.slug); // Pre-computed slug list

// Populate maps for ultra-fast access
courseDetails.forEach(course => {
  courseMapById.set(course.id, course);
  courseMapBySlug.set(course.slug, course);
});

// ULTRA-OPTIMIZED HELPER FUNCTIONS

// Basic course retrieval by slug - O(1) lookup
export const getCourseBySlug = (slug) => {
  return courseMapBySlug.get(slug) || null;
};

// Basic course retrieval by ID - O(1) lookup
export const getCourseById = (id) => {
  return courseMapById.get(parseInt(id)) || null;
};

// OPTIMIZED: Get similar courses using predefined IDs with batch lookup
export const getSimilarCourses = (courseSlug, limit = 6) => {
  const course = courseMapBySlug.get(courseSlug);
  if (!course?.similarCourseIds) return [];
  
  // Batch lookup with early termination and validation
  const similarCourses = [];
  const targetLimit = Math.min(limit, course.similarCourseIds.length);
  
  for (let i = 0; i < targetLimit; i++) {
    const similarCourse = courseMapById.get(course.similarCourseIds[i]);
    if (similarCourse && similarCourse.slug !== courseSlug) {
      similarCourses.push(similarCourse);
    }
  }
  
  return similarCourses;
};

// OPTIMIZED: Direct syllabus access
export const getSyllabusByCourseSlug = (courseSlug) => {
  const course = courseMapBySlug.get(courseSlug);
  return course?.courseSyllabus || [];
};

